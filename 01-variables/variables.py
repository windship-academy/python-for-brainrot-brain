# Una variable es un contenedor de un valor
# Tipos principales: String, Integer, Float, Boolean
# ------------------------------
# STRINGS - texto
pet_name = "suko"            # nombre de tu mascota
favorite_movie = 'WALL·E' # película favorita
username = "coder123"        # nombre de usuario
country = "España"           # país

# Imprimir strings
print("----- STRINGS -----")
print(pet_name)
print(favorite_movie)
print(f"Hola, soy {username} de {country}")
print(f"Mi mascota se llama {pet_name} y mi película favorita es {favorite_movie}")

# ------------------------------
# INTEGERS - números enteros
books_read = 12
friends_count = 7
current_year = 2025

# Imprimir integers
print("----- INTEGERS -----")
print(f"He leído {books_read} libros este año")
print(f"Tengo {friends_count} amigos")
print(f"El año actual es {current_year}")

# ------------------------------
# FLOATS - números con decimales
temperature = 23.5           # temperatura actual
coffee_price = 1.25          # precio del café
pi_value = 3.14159           # valor aproximado de pi

# Imprimir floats
print("----- FLOATS -----")
print(f"La temperatura es de {temperature}°C")
print(f"El precio del café es ${coffee_price}")
print(f"El valor aproximado de pi es {pi_value}")

